/**
 * ================================================================================================
 * TIMEZONE SELECTOR COMPONENT - Компонент выбора таймзоны
 * ================================================================================================
 *
 * ЦЕЛЬ: Переиспользуемый компонент выбора таймзоны для устранения дублирования разметки.
 *
 * ПРОБЛЕМА: Разметка выбора таймзоны (список опций) дублировалась в нескольких местах
 * (статические примеры модальных окон, реальные модальные окна), что нарушало принцип
 * "единого источника правды" (Single Source of Truth).
 *
 * РЕШЕНИЕ: Вынос разметки выбора таймзоны в отдельный переиспользуемый компонент.
 * Все инстансы модальных окон используют один и тот же компонент, что гарантирует
 * единообразие и упрощает поддержку.
 *
 * ПРИНЦИПЫ:
 * - Единый источник правды: список таймзон определен в одном месте (шаблон компонента)
 * - Переиспользование: компонент используется во всех местах, где нужен выбор таймзоны
 * - Двусторонняя привязка: поддержка v-model через props и emit
 *
 * API КОМПОНЕНТА:
 *
 * Входные параметры (props):
 * - modelValue (String, required) — текущее значение таймзоны (для v-model)
 *
 * Выходные события (emits):
 * - update:modelValue — обновление значения таймзоны (для v-model)
 *
 * ССЫЛКИ:
 * - Шаблон: shared/templates/timezone-selector-template.js
 * - Принцип единого источника правды: docs/doc-comp-principles.md
 */

window.cmpTimezoneSelector = {
    template: '#timezone-selector-template',

    props: {
        modelValue: {
            type: String,
            required: true
        }
    },

    emits: ['update:modelValue']
};

